// ── MOTEUR DE RECHERCHE ──
function searchNAF(query) {
  if (!query || query.trim().length < 2) return null;
  const q = query.toLowerCase()
    .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
    .trim();

  // 1. Correspondance exacte
  for (const [key, val] of Object.entries(NAF_DICT)) {
    const k = key.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    if (k === q) return val;
  }

  // 2. Commence par
  for (const [key, val] of Object.entries(NAF_DICT)) {
    const k = key.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    if (k.startsWith(q) || q.startsWith(k)) return val;
  }

  // 3. Contient
  for (const [key, val] of Object.entries(NAF_DICT)) {
    const k = key.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    if (k.includes(q) || q.includes(k)) return val;
  }

  // 4. Recherche dans les labels
  for (const [key, val] of Object.entries(NAF_DICT)) {
    const lbl = val.label.toLowerCase()
      .normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    if (lbl.includes(q)) return val;
  }

  return null;
}

